#!/usr/bin/env node
/* global __dirname */

const fs = require("fs");
const path = require("path");

const rootDir = path.resolve(__dirname, "..");
const outputPath = path.join(rootDir, "src", "features", "reader", "webview", "vendorScripts.ts");

const vendors = [
  {
    exportName: "JSZIP_SCRIPT",
    packageName: "jszip",
    packagePath: "jszip/package.json",
    distPath: "jszip/dist/jszip.min.js"
  },
  {
    exportName: "EPUBJS_SCRIPT",
    packageName: "epubjs",
    packagePath: "epubjs/package.json",
    distPath: "epubjs/dist/epub.min.js"
  }
];

function readVendor(vendor) {
  const packageJsonPath = require.resolve(vendor.packagePath, { paths: [rootDir] });
  const packageJson = JSON.parse(fs.readFileSync(packageJsonPath, "utf8"));
  const scriptPath = require.resolve(vendor.distPath, { paths: [rootDir] });
  const script = fs.readFileSync(scriptPath, "utf8").replace(/<\/script/gi, "<\\/script");

  return {
    ...vendor,
    version: packageJson.version,
    license: packageJson.license,
    script
  };
}

const vendorData = vendors.map(readVendor);
const output = `// Generated by scripts/generate-reader-vendor-assets.js. Do not edit manually.

${vendorData
  .map(
    (vendor) => `// ${vendor.packageName}@${vendor.version} (${vendor.license})
export const ${vendor.exportName} = ${JSON.stringify(vendor.script)};
`
  )
  .join("\n")}
`;

fs.writeFileSync(outputPath, output);
console.log(`Generated ${path.relative(rootDir, outputPath)} from ${vendorData.map((vendor) => `${vendor.packageName}@${vendor.version}`).join(", ")}.`);
